# Definir una lista que almacene por asignación los nombres de 5 personas.
# Contar cuantos de esos nombres tienen 5 o más caracteres.


list = ["juan", "pedro", "roberto", "jaimito", "fulanito"]

cont = 0
x = 0 
while x < len(list):
    if len(list[x],)>= 5:
        cont = cont + 1 
    x = x + 1     
print(cont)         